print("浮点数除法")
print("1.0 / 2 = ", 1.0 / 2)

# Python提供了另外一个用于实现整除的操作符 -- 双斜杠
print("整除使用双斜杠")
print("1 // 2 = ", 1 // 2)

print("取余运算")
print("10 / 3 = ", 10 % 3)
print("2.75 % 0.5 = ", 2.75 % 0.5)

print("幂运算")
print("2 ** 3 = ", 2 ** 3)
print("-3 ** 2 = ", -3 ** 2)
print("(-3) ** 2 = ", (-3) ** 2)

# 普通整数不能大于2 147 483 647 （也不能小于-2 147 483 648），如果真的需要大数，可以使用长整数型
print("长整数")
print(1000000000000000)

print("十六进制")
print("0xAF = ", 0xAF)
print("八进制")
print("0o10 = ", 0o10)
