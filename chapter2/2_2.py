# 变量名不能包含空格，但可以使用下划线来分隔其中的单词
# 不要将python关键字和函数名用作变量名，即不要使用python保留用于特殊用途的单词，如print
# 变量名应既简单又具有描述性。例如，name比n好，student_name比s_n好
# 慎用小写字母l和大写字母Q，因为它们可能被人错看成数字1和0

